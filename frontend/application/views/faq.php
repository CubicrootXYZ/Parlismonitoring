    <div class="card">
    <div class="card-header ">
    Die Daten stimmen nicht auf allen Seiten überein. Wieso das?
    </div>
    <div class="card-body">
    Wir nutzen unterschiedliche Methoden um die Daten anzuzeigen, daher kann es zu Abweichungen kommen. Ebenso speichern wir Ergebnisse für einen gerinen Zeitraum zwischen um die Anfragen an unsere Datenbank 
    zu minimieren. <br><br>
    Besonders auffällig sind die Unterschiede zwischen den Trends und der Startseite. Für die Startseite nutzen wir eine exakte Suche, die Wörter müssen also vollständig (Where) übereinstimmen, wohingegen wir uns bei den Trends 
    sogennenater "Wildcards" bedienen, hier muss nur das Wort die gesuchte Phrase beinhalten (Like). Ebenso basieren  die Auswertungen auf der Startseite nur auf den Titeln der Dokumente und nicht auch auf den Dateiinhalten.
    </div>
