    Wie zuverlässig sind diese Daten?
    </div>
    <div class="card-body">
    Die Daten durchlaufen mehrere Schritte die durchaus auch fehlerbehaftet sein können. Zum einen können Fehler beim sogenannten "parsen", dem Auslesen des Dokumentenmanagementsystems, entstehen. Besonders Änderungen 
    an der Website des Systems können zu Fehlern führen. Weiter werden die Inhalte durch einen sogennanten "Lemmatizer" analysiert, hierbei wird versucht unterschiedliche Schreibweisen eines Wortes auf ein Stammwort zurückzuführen und 
    Nomen zu identifizieren. Auch die Formatierung der Dateien kann zu Fehlern führen. Daher haben wir uns entschieden für die Trends lediglich auf die Titel der Dokumente zurückzugreifen. <br><br>
    Es ist auch zu beachten, dass dieses Tool maximal 30 Dokumente pro Durchlauf aufnehmen kann, werden mehr Dokumente zwischen 2 Durchläufen veröffentlicht werden nur die letzten 30 analysiert. Auch kann 
    die Software zeitweise abgeschalten sein.
    </div>
